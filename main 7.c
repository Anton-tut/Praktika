//Тема: два кола

//Дано два кола, визначте кількість точок перетину

//Вхідні дані: шість чисел х1, y1, r1, х2, y2, r2, де х1, y1, х2, y2 - координати центрів, а r1 та r2 - їх радіуси.

//Вихідні дані: кількість точок перетину. Якщо точок перетину безкінечно багато, то вивести -1.


#include <stdio.h>
#include <math.h>

int countIntersectionPoints(int x1, int y1, int r1, int x2, int y2, int r2) {
    // Обчислення відстані між центрами колів
    double distance = sqrt((x2 - x1) * (x2 - x1) + (y2 - y1) * (y2 - y1));

    // Визначення відношку радіусів колів
    double radiusRatio = (double)r1 / r2;

    // Визначення кількості точок перетину
    if (distance == 0 && r1 == r2) {
        // Кола ідентичні (мають спільний центр і однаковий радіус)
        return -1;
    } else if (distance > r1 + r2 || distance < fabs(r1 - r2)) {
        // Кола не перетинаються
        return 0;
    } else if (distance == r1 + r2 || distance == fabs(r1 - r2)) {
        // Кола торкаються зовні або всередині одне одного
        return 1;
    } else {
        // Кола перетинаються в двох точках
        return 2;
    }
}

int main() {
    int x1, y1, r1, x2, y2, r2;
    printf("Введіть координати центра першого кола (x1, y1): ");
    scanf("%d %d", &x1, &y1);

    printf("Введіть радіус першого кола: ");
    scanf("%d", &r1);

    printf("Введіть координати центра другого кола (x2, y2): ");
    scanf("%d %d", &x2, &y2);

    printf("Введіть радіус другого кола: ");
    scanf("%d", &r2);

    int result = countIntersectionPoints(x1, y1, r1, x2, y2, r2); // Обчислення кількості точок перетину

    printf("Кількість точок перетину: %d\n", result);

    return 0;
}

