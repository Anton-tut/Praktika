//Тема: задача з одиницями

//Обчислити кількість послідовностей довжиною n, що складаються лише з нулів і одиниць, де не зустрічається три одиниці підряд.

//Вхідні дані: Довжина послідовностей n (1 < n < 10000)

//Вихідні дані: Вивести кількість шуканих послідовностей за модулем 12345.




#include <stdio.h>

// Функція для обчислення кількості послідовностей довжиною n
int countSequences(int n) {
    int dp[n+1]; // Масив для зберігання кількостей послідовностей
    dp[0] = 1; // Базовий випадок: одна послідовність довжиною 0 (порожня послідовність)
    dp[1] = 2; // Базовий випадок: дві послідовності довжиною 1 - 0 або 1
    dp[2] = 3; // Базовий випадок: три послідовності довжиною 2 - 00, 01, 10

    // Обчислення кількості послідовностей для довжин більше 2
    for (int i = 3; i <= n; i++) {
        dp[i] = (dp[i-1] + dp[i-2]) % 12345; // Кількість послідовностей залежить від попередніх двох довжин
    }

    return dp[n]; // Повертаємо кількість послідовностей довжиною n
}

int main() {
    int n;
    printf("Введіть довжину послідовності n: ");
    scanf("%d", &n);

    int result = countSequences(n);

    printf("Кількість шуканих послідовностей за модулем 12345: %d\n", result);

    return 0;
}
